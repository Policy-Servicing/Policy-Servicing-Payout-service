-- Core policy transaction table
CREATE TABLE IF NOT EXISTS POLICY_TRANSACTION (
  POLICY_NO              VARCHAR2(50)    NOT NULL,
  REQUEST_ID             VARCHAR2(50)    NOT NULL,
  MODULE_NAME            VARCHAR2(100),
  CUSTOMER_NAME          VARCHAR2(200),
  PRODUCT_ID             VARCHAR2(50),
  CURRENT_STAGE          VARCHAR2(30)    NOT NULL,
  PAYOUT_AMOUNT          NUMBER(18,2),
  DATE_OF_COMMENCEMENT   DATE,
  MATURITY_DATE          DATE,
  CONSTRAINT PK_POLICY_TXN PRIMARY KEY (POLICY_NO, REQUEST_ID)
);

-- Stage history table
CREATE TABLE IF NOT EXISTS POLICY_STAGE_HISTORY (
  HISTORY_ID   NUMBER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  POLICY_NO    VARCHAR2(50)  NOT NULL,
  REQUEST_ID   VARCHAR2(50)  NOT NULL,
  STAGE_NAME   VARCHAR2(30)  NOT NULL,
  STATUS       VARCHAR2(20)  NOT NULL,  -- COMPLETED | CURRENT | PENDING
  ENTRY_DATE   TIMESTAMP,
  COMPLETED_AT TIMESTAMP
);

CREATE INDEX IDX_PSH_POLICY ON POLICY_STAGE_HISTORY(POLICY_NO, REQUEST_ID);
