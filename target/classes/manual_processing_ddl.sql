-- Audit table: records every manual approve / sanction action
CREATE TABLE AUDIT_MANUAL_PROCESSING (
  AUDIT_ID       NUMBER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  POLICY_NO      VARCHAR2(50)   NOT NULL,
  REQUEST_ID     VARCHAR2(50)   NOT NULL,
  ACTION_TAKEN   VARCHAR2(20)   NOT NULL,
  PERFORMED_BY   VARCHAR2(100)  NOT NULL,
  REMARKS        VARCHAR2(200),
  PERFORMED_AT   TIMESTAMP      DEFAULT SYSTIMESTAMP,
  STATUS         VARCHAR2(20)   DEFAULT 'SUCCESS'
);

-- Indexes for common query patterns
CREATE INDEX IDX_AMP_POLICY ON AUDIT_MANUAL_PROCESSING(POLICY_NO);
CREATE INDEX IDX_AMP_DATE   ON AUDIT_MANUAL_PROCESSING(PERFORMED_AT);

